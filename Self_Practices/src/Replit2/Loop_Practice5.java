/*
4. write a program that can calculate the sum of all numbers between 1 to 100
 */

public class Loop_Practice5 {
    public static void main(String[] args) {
        int a =1;
        int sum = 0;
        for( a=1; a<=100; a++){
         sum = sum +a;
        }
        System.out.println(sum);
    }
}
